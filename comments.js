// create web server
// 1. load express module
const express = require('express')
// 2. create web server
const app = express()
// 3. set port
const port = 3000
// 4. handle request and response
app.get('/', (req, res) => {
  res.send('Hello World!')
})
// 5. start server
app.listen(port, () => {
  console.log(`Express is listening on http://localhost:${port}`)
})
